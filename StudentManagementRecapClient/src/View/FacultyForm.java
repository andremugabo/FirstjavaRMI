    private void RegisterFacultyBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterFacultyBTNActionPerformed
      ///Validation
        try {
            Registry reg=LocateRegistry.getRegistry("127.0.0.1", 7000);
            FacultyServise facultys=(FacultyServise) reg.lookup("facultycall");
            Faculty dm =new Faculty();
            dm.setFacname(Facultyname.getText());
            Dean adddataitem=(Dean) jComboBox.getSelectedItem();
            dm.setDean(adddataitem);
            
            Faculty thefaculty=facultys.Registerdn(dm);
            if(thefaculty!=null){
                JOptionPane.showMessageDialog(this, "Data Saved Successful");
            }else{
                 JOptionPane.showMessageDialog(this, "Data not Saved");
            }
            
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_RegisterFacultyBTNActionPerformed

