    private void adddata(){
        try {
            Registry reg=LocateRegistry.getRegistry("127.0.0.1", 7000);
            Deanservise deans=(Deanservise) reg.lookup("Deancall");
           List<Dean> thedean=deans.deandata();
           if(thedean!=null){
               for(Dean deandata:thedean){
                   jComboBox.addItem(deandata);  
               }
           }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        
    }
