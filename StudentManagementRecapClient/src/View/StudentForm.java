    private void adddatadepartment(){
        try {
            Registry reg=LocateRegistry.getRegistry("127.0.0.1", 7000);
           DepartmentService facs=(DepartmentService) reg.lookup("Departmentcall");
           List<Department> thedep=facs.deandata();
           if(thedep!=null){
               for(Department depts:thedep){
                   departmentlist.addItem(depts);  
               }
           }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        
    }
    private void adddatacourse(){
        try {
            Registry reg=LocateRegistry.getRegistry("127.0.0.1", 7000);
            Courseservice facs=(Courseservice) reg.lookup("Coursecall");
           List<Course> thecour=facs.deandata();
           if(thecour!=null){
               for(Course depts:thecour){
                  defcourselist.addElement(depts);
               }
               courselistdata.setModel(defcourselist);
           }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        
    }
