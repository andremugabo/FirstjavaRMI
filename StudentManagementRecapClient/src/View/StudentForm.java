    private void RegisterFacultyBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterFacultyBTNActionPerformed
      ///Validation
        try {
            Registry reg=LocateRegistry.getRegistry("127.0.0.1", 7000);
            StudentService deptservice=(StudentService) reg.lookup("Studentcall");
            Student dm =new Student();
            dm.setStudentId(studentid.getText());
            dm.setNames(studentname.getText());
            Department adddataitem= (Department) departmentlist.getSelectedItem();
            dm.setDepartment(adddataitem);
            Set<Course> courseset=new  HashSet<>();
            List<Course> courselist=  courselistdata.getSelectedValuesList();
            for(Course thecourse:courselist){
                courseset.add(thecourse);
            }
            dm.setDepartment(adddataitem);
            dm.setCourses(courseset);
            Student thedep=deptservice.Registerdn(dm);
            if(thedep!=null){
                JOptionPane.showMessageDialog(this, "Data Saved Successful");
            }else{
                 JOptionPane.showMessageDialog(this, "Data not Saved");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_RegisterFacultyBTNActionPerformed

