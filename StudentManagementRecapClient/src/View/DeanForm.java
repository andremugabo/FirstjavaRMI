    private void displaydata(){
        
        try {
            Registry reg=LocateRegistry.getRegistry("127.0.0.1", 7000);
           Deanservise facs=(Deanservise) reg.lookup("Deancall");
           List<Dean> thedean=facs.deandata();
            DefaultTableModel tmodel=(DefaultTableModel)Deantabledata.getModel();
            tmodel.setRowCount(0);
           if(thedean!=null){
               for(Dean deans:thedean){
                   Object data[]={
                       
                       deans.getDeanId(),
                       deans.getName()
                   };
                   tmodel.addRow(data);
                  
               }
           }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

