    private void RegisterDeanBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterDeanBTNActionPerformed
      ///Validation
        try {
            Registry reg=LocateRegistry.getRegistry("127.0.0.1", 7000);
            Deanservise deans=(Deanservise) reg.lookup("Deancall");
            Dean dm=new Dean();
            dm.setName(Deanname.getText());
            Dean thedean=deans.Registerdn(dm);
            
            if(thedean!=null){
                JOptionPane.showMessageDialog(this, "Data Saved Successful");
            }else{
                 JOptionPane.showMessageDialog(this, "Data not Saved");
            }
            
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_RegisterDeanBTNActionPerformed

    private void SearchDeanBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchDeanBTNActionPerformed
        try{
        Registry reg=LocateRegistry.getRegistry("127.0.0.1", 7000);
           Deanservise facs=(Deanservise) reg.lookup("Deancall");
          Dean thedean=new Dean();
          thedean.setDeanId(Integer.parseInt(Deanid.getText()));
           Dean deanobj=facs.searchingdn(thedean);
           if(deanobj!=null){
               Deanname.setText(deanobj.getName());
           }else{
               JOptionPane.showMessageDialog(this,"Dean ID not Exist! Register Now");
        }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_SearchDeanBTNActionPerformed

    private void UpdateDeanBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateDeanBTNActionPerformed
         try {
            Registry reg=LocateRegistry.getRegistry("127.0.0.1", 7000);
            Deanservise deans=(Deanservise) reg.lookup("Deancall");
            Dean dm=new Dean();
            dm.setName(Deanname.getText());
            dm.setDeanId( Integer.parseInt(Deanid.getText()));
            Dean thedean=deans.updatedn(dm);
            
            if(thedean!=null){
                JOptionPane.showMessageDialog(this, "Data Updated Successful");
                Deanname.setText("");
                 displaydata();
            }else{
                 JOptionPane.showMessageDialog(this, "Data not Updated");
            }
            
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_UpdateDeanBTNActionPerformed

